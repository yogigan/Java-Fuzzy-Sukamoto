    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        try {
            Connection cn = DriverManager.getConnection("jdbc:mysql://localhost/db_siswa", "root", "");
            String sql = "Insert into nilaisiswa (nis, nilai_mtk, nilai_ing, nilai_ind, nilai_ratarata) "
                    + "values (?,?,?,?,?)";
            PreparedStatement pst = cn.prepareStatement(sql);
            pst.setInt(1, Integer.parseInt(txtNIS.getText()));
            pst.setDouble(2, Double.parseDouble(nilai_mtk.getText()));
            pst.setDouble(3, Double.parseDouble(nilai_ing.getText()));
            pst.setDouble(4, Double.parseDouble(nilai_ind.getText()));
            pst.setDouble(5, (Double.parseDouble(nilai_mtk.getText()) + Double.parseDouble(nilai_ing.getText())
                    + Double.parseDouble(nilai_ind.getText())) / 3);
            pst.execute();
            tampilkan(" ");
            reset();
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Ada data yang belum diisi");
        }
    }//GEN-LAST:event_btnSimpanActionPerformed

