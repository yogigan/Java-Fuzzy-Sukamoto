    private void tampilkan(String kelas) {
        int row = tblNilai.getRowCount();
        for (int a = 0; a < row; a++) {
            tbl.removeRow(0);
        }
        try {
            Connection cn = DriverManager.getConnection("jdbc:mysql://localhost/db_siswa", "root", "");
            String query;
            if (kelas == " ") {
                query = "SELECT datasiswa.nis, datasiswa.kelas, nilaisiswa.nilai_mtk, "
                        + "nilaisiswa.nilai_ind, nilaisiswa.nilai_ing, nilaisiswa.nilai_ratarata "
                        + "FROM datasiswa INNER JOIN nilaisiswa ON datasiswa.nis = nilaisiswa.nis";
            } else {
                query = "SELECT datasiswa.nis, datasiswa.kelas, nilaisiswa.nilai_mtk, "
                        + "nilaisiswa.nilai_ind, nilaisiswa.nilai_ing, nilaisiswa.nilai_ratarata "
                        + "FROM datasiswa INNER JOIN nilaisiswa ON datasiswa.nis = nilaisiswa.nis WHERE datasiswa.kelas = '" + kelas + "'";
            }
            ResultSet rs = cn.createStatement().executeQuery(query);
            while (rs.next()) {
                String data[] = {rs.getString(1), rs.getString(2), rs.getString(3), rs.getString(4), rs.getString(5), rs.getString(6)};
                tbl.addRow(data);
            }
        } catch (Exception e) {
        }

        btnUbah.setEnabled(false);
        btnHapus.setEnabled(false);
    }

