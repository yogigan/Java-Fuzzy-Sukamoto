    private void btnCariActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCariActionPerformed
        // TODO add your handling code here:
        int row = tblSiswa.getRowCount();
        for (int a = 0; a < row; a++) {
            tblSiswa.removeRow(0);
        }
        try {
            Connection cn = DriverManager.getConnection("jdbc:mysql://localhost/db_siswa", "root", "");
            ResultSet rs = cn.createStatement().executeQuery("SELECT nis, nama, kelas, tempat_lahir, tanggal_lahir, jenkel, no_hp, nama_ortu, tanggungan, pekerjaan_ortu, penghasilan_ortu from datasiswa where nis ='" + txtNIS.getText() + "'");
            while (rs.next()) {
                String data[] = {rs.getString(1), rs.getString(2), rs.getString(3), rs.getString(4), rs.getString(5), rs.getString(6), rs.getString(7), rs.getString(8), rs.getString(9), rs.getString(10), rs.getString(11)};
                tblSiswa.addRow(data);
            }
        } catch (Exception ex) {
            Logger.getLogger(FormNilaiSiswa.class.getName()).log(Level.SEVERE, null, ex);
        }
        btnSimpan.setEnabled(false);
        btnUbah.setEnabled(true);
        btnHapus.setEnabled(true);
        btnCari.setEnabled(true);
    }//GEN-LAST:event_btnCariActionPerformed

