    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        // TODO add your handling code here:
        try {
            Connection cn = DriverManager.getConnection("jdbc:mysql://localhost/db_siswa", "root", "");
            String sql = "Insert into datasiswa (nis, nama, kelas, tempat_lahir, tanggal_lahir, jenkel, no_hp, nama_ortu, tanggungan, pekerjaan_ortu, penghasilan_ortu)"
                    + "values (?,?,?,?,?,?,?,?,?,?,?)";
            PreparedStatement pst = cn.prepareStatement(sql);

            pst.setLong(1, Long.parseLong(txtNIS.getText()));
            pst.setString(2, txtNama.getText());
            pst.setString(3, (String) cbKelas.getSelectedItem());
            pst.setString(4, txtTempatLahir.getText());

            SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
            String date = sdf.format(txtTanggalLahir.getDate());
            pst.setString(5, date);

            rbLakilaki.setActionCommand("Laki-laki");
            rbPerempuan.setActionCommand("Perempuan");
            pst.setString(6, buttonGroupJK.getSelection().getActionCommand());

            pst.setLong(7, Long.parseLong(txtHp.getText()));
            pst.setString(8, txtNamaOrtu.getText());
            pst.setInt(9, Integer.parseInt(txtTanggungan.getText()));
            pst.setString(10, txtPekerjaan.getText());
            pst.setLong(11, Long.parseLong(txtPenghasilan.getText()));
            pst.execute();
            tampilkanTabel();
            reset();

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(rootPane, ex);
            Logger.getLogger(DataSiswa.class.getName()).log(Level.SEVERE, null, ex);
        }
        btnSimpan.setEnabled(false);
        btnUbah.setEnabled(false);
        btnHapus.setEnabled(false);
        btnCari.setEnabled(true);
        JOptionPane.showMessageDialog(null, "Data berhasil ditambahkan!");
    }//GEN-LAST:event_btnSimpanActionPerformed

